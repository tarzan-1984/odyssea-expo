const EmojiPicker: React.FC<EmojiPickerProps> = ({ isOpen, onClose, onEmojiSelect }) => {
  const [selectedCategory, setSelectedCategory] = useState(0);

  if (!isOpen) return null;

  const handleEmojiSelect = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  return (
    <Modal
      transparent
      visible={isOpen}
      onRequestClose={onClose}
      animationType="slide"
    >
      <TouchableOpacity
        style={styles.overlay}
        activeOpacity={1}
        onPress={onClose}
      >
        <View style={styles.container} onStartShouldSetResponder={() => true}>
          {/* Header */}
          <View style={styles.header}>
            <Text style={styles.headerTitle}>Choose emoji</Text>
            <TouchableOpacity onPress={onClose} style={styles.closeButton}>
              <Text style={styles.closeButtonText}>âœ•</Text>
            </TouchableOpacity>
          </View>

          {/* Category tabs */}
          <ScrollView
            horizontal
            showsHorizontalScrollIndicator={false}
            style={styles.categoryTabs}
            contentContainerStyle={styles.categoryTabsContent}
          >
            {EMOJI_CATEGORIES.map((category, index) => (
              <TouchableOpacity
                key={index}
                onPress={() => setSelectedCategory(index)}
                style={[
                  styles.categoryTab,
                  selectedCategory === index && styles.categoryTabActive,
                ]}
              >
                <Text
                  style={[
                    styles.categoryTabText,
                    selectedCategory === index && styles.categoryTabTextActive,
                  ]}
                >
                  {category.name.split(" ")[0]}
                </Text>
              </TouchableOpacity>
            ))}
          </ScrollView>

          {/* Emojis grid */}
          <FlatList
            data={EMOJI_CATEGORIES[selectedCategory].emojis}
            numColumns={8}
            keyExtractor={(item, index) => `${selectedCategory}-${index}`}
            contentContainerStyle={styles.emojiGrid}
            renderItem={({ item }) => (
              <TouchableOpacity
                style={styles.emojiButton}
                onPress={() => handleEmojiSelect(item)}
                activeOpacity={0.7}
              >
                <Text style={styles.emoji}>{item}</Text>
              </TouchableOpacity>
            )}
          />
        </View>
      </TouchableOpacity>
    </Modal>
  );
};

